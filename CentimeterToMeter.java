import java.util.Scanner;
public class CentimeterToMeter {
	public static void main (String[] args)  
    { 
        double cm, meter; 
        cm = 1000; 
   
        meter = cm / 100.0; 
        
      
        System.out.println("Length in meter = " + 
                                    meter + "m"); 
          
      
         
    } 
} 
  


